Graph.breadthFirst = function(x,y){
	let queue = [];
	let visited = new Set();
	queue.push({x:x,y:y});
	visited.add(x+"_"+y);
	this.tree = new Tree(x+"_"+y);
	while(queue.length>0){
		let loc = queue.pop();
		let loc_x = loc.x;
		let loc_y = loc.y;
		let parent_key = loc_x + "_" + loc_y;
		for(let i=-1;i<=1;i++){
			for(let j=-1;j<=1;j++){
				let new_x = loc_x + i;
				let new_y = loc_y + j;
				if(i!=0 && j!=0){
					let key = new_x+"_"+new_y;
					if(!visited.has(key) && this.graph[new_x][new_y]==0){
						queue.push({x:new_x,y:new_y});
						visited.add(key);
						this.tree.push(parent_key,key);
					}
				}
			}
		}
	}
}


Graph.getTree = function(arr,start,end){
	this.graph = arr;
	this.end = end;
	this.breadthFirst(start.x,start.y);
	return this.tree;
}


