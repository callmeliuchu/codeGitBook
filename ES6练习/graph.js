class Tree{
	constructor(root){
		this.root = root;
		this.tree = new Map();
	}
	push(parent,child){
			let arr = this.tree.get(parent);
			if(typeof arr != 'object'){
				arr = [];
				arr.push(child);
				this.tree.set(parent,arr);
			}else{
				this.tree.get(parent).push(child);
			}
	}
}



