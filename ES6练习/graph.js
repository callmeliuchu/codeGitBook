function show(arr){
    	var s = "";
    	for(let i=0;i<arr.length;i++){
    		var temp = "";
    		for(let j=0;j<arr[i].length;j++){
    			if(j == 0){
    				temp = temp + arr[i][j];
    			}else{
    			    temp = temp + ',' + arr[i][j];
    			}
    		}
    		if(i==0){
    			s = s + "[" + temp + "]";
    		}else{
    			s = s + ",\n[" + temp + "]";
    		}
    	}
    	s = "[" + s + "]";
    	print(s);
}

function copyFromArr(arr){
	let arr1 = new Array(arr.length);
	for(let i=0;i<arr.length;i++){
		arr1[i] = new Array();
		for(let j=0;j<arr[i].length;j++){
			arr1[i][j] = arr[i][j];
		}
	}
	return arr1;
}

var Graph = {};
Graph.flag = false;
Graph.deepthFirst = function(x,y){
	if(Graph.flag){
		return;
	}
	if(x == this.end.x && y == this.end.y){
		Graph.flag = true;
		Graph.path = copyFromArr(this.graph);
		return;
	}
	if(this.graph[x+1][y] == 0){
		this.graph[x+1][y] = 2;
		this.deepthFirst(x+1,y);
		this.graph[x+1][y] = 0;
	}
	if(this.graph[x][y+1] == 0){
		this.graph[x][y+1] = 2; 
		this.deepthFirst(x,y+1);
		this.graph[x][y+1] = 0; 
	}
	if(this.graph[x-1][y] == 0){
		this.graph[x-1][y] = 2; 
		this.deepthFirst(x-1,y);
		this.graph[x-1][y] = 0; 
	}
	if(this.graph[x][y-1] == 0){
		this.graph[x][y-1] = 2; 
		this.deepthFirst(x,y-1);
		this.graph[x][y-1] = 0; 
	}
}

